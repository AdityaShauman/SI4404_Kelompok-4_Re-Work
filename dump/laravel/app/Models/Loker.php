    use HasFactory;

    protected $fillable = [
        'nama',
        'deksripsi',
        'foto',
        'link',
    ];

    /**
     * The attributes that should be cast.
     *
     * @var array<string, string>
     */
    protected $casts = [
        'created_at' => 'datetime',
        'updated_at' => 'datetime'
    ];
}
